class Tramite {
  final String idTramiteDetalle,
      nroExpediente,
      fecha,
      idTipoDocumento,
      tipoDocumento,
      nroDocumento,
      nroFolios,
      nombreOrigen,
      oficinaDestino,
      asunto,
      adjuntoOtros;

  Tramite(
      {this.idTramiteDetalle,
      this.nroExpediente,
      this.fecha,
      this.idTipoDocumento,
      this.tipoDocumento,
      this.nroDocumento,
      this.nroFolios,
      this.nombreOrigen,
      this.oficinaDestino,
      this.asunto,
      this.adjuntoOtros});

  factory Tramite.fromJson(Map<String, dynamic> json) {
    return Tramite(
      idTramiteDetalle: json["IdTramiteDetalle"],
      nroExpediente: json["NroExpediente"],
      fecha: json["Fecha"],
      idTipoDocumento: json["IdTipoDocumento"],
      tipoDocumento: json["TipoDocumento"],
      nroDocumento: json["NroDocumento"],
      nroFolios: json["NroFolios"],
      nombreOrigen: json["NombreOrigen"],
      adjuntoOtros: json["Adjunto_Otros"],
      oficinaDestino: json["OficinaDestino"],
      asunto: json["Asunto"],
    );
  }
}

